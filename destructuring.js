//array
const arr = ["Fika", "Ahmad", "Lutfi"];

//destructuring
const [name1, name2, name3] = arr;

//print output
console.log(name1); // <-- "Fika"
console.log(name3); // <-- "Ahmad"
console.log(name3); // <-- "Lutfi"

//object
const obj = {
  name: "Fika Ridaul Maulayya",
  age: 25,
};

//destructuring
const { name, age } = obj;

//print ouput
console.log(name); // <-- "Fika Ridaul Maulayya"
console.log(age); // <-- "25"
